package practicing014;

public class Incremento {

	public static void main(String[] args) {
		
		int i = 6;
		System.out.println(i);
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(i);
		System.out.println(i--);
		System.out.println(i);
		System.out.println(++i);
		
	}
	
}
